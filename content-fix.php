<h1 align="center">Counting Game</h1>

<div class="row">

<div class="col-md-12">
	<p>The Counting Game was commissioned by Western Front Gallery
	to mark the inauguration of the Vancouver New Public Library in 1995. The idea was to create music for choir which would favour the spatial properties of the new building. Archways, stairs, foyers and esplanades all make for different acoustic environments.</p>
</div>

<div class="col-md-7">
	<p>Five musical sections of approximately 4 min.were created to make a 20 min. piece performed for a choir of 100 and radio. The soundtrack (musical accompaniment for the choir) was played on a low-watt fm radio transmitter, so that the choir could move freely from one end of the building site to the other . Each section was given a number, from 1 to 8.</p>
</div>

